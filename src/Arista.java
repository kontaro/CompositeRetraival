public class Arista {
    
    int indiceUno;
    int indiceDos;
    float interValue;
    
    public Arista(int indiceUno, int indiceDos, float interValue){
        this.indiceDos=indiceDos;
        this.indiceUno=indiceUno;
        this.interValue=interValue;
    }

    public int getIndiceDos() {
        return indiceDos;
    }

    public int getIndiceUno() {
        return indiceUno;
    }

    public float getInterValue() {
        return interValue;
    }
    
    
    
}
